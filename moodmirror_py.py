import streamlit as st
from transformers import pipeline

# Load sentiment analysis model
sentiment_pipeline = pipeline("sentiment-analysis")

# Streamlit app
st.set_page_config(page_title="MoodMirror", layout="centered")
st.title("üß† MoodMirror ‚Äì Emotional Health Checker")
st.write("Type a journal entry or message to analyze its emotional tone.")

user_input = st.text_area("Enter your text here:", height=200)

if st.button("Analyze"):
    if user_input.strip() == "":
        st.warning("Please enter some text.")
    else:
        result = sentiment_pipeline(user_input)[0]
        label = result["label"]
        score = result["score"]

        st.subheader("üí¨ Sentiment Result")
        st.write(f"**Sentiment:** {label} ({score*100:.2f}%)")

        if label == "NEGATIVE":
            st.error("‚ö†Ô∏è Your message indicates distress or negative emotions.")
            st.info("üßò Tip: Consider talking to someone or taking a short mindfulness break.")
        else:
            st.success("üòä Your message seems positive!")

st.markdown("---")
st.caption("Not a substitute for professional diagnosis.")




